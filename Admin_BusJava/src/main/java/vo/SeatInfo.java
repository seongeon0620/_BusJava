package vo;

public class SeatInfo {
	private int rd_idx, rd_seat_num;
	private String ri_idx;
	
	public int getRd_idx() {
		return rd_idx;
	}
	public void setRd_idx(int rd_idx) {
		this.rd_idx = rd_idx;
	}
	public int getRd_seat_num() {
		return rd_seat_num;
	}
	public void setRd_seat_num(int rd_seat_num) {
		this.rd_seat_num = rd_seat_num;
	}
	public String getRi_idx() {
		return ri_idx;
	}
	public void setRi_idx(String ri_idx) {
		this.ri_idx = ri_idx;
	}
	
	
}
