package vo;

public class TerminalInfo {
	private int bt_code;
	private String bt_name, bt_area, bt_addr, bt_status;
	private double bt_lat, bt_lon;
	public int getBt_code() {
		return bt_code;
	}
	public void setBt_code(int bt_code) {
		this.bt_code = bt_code;
	}
	public String getBt_name() {
		return bt_name;
	}
	public void setBt_name(String bt_name) {
		this.bt_name = bt_name;
	}
	public String getBt_area() {
		return bt_area;
	}
	public void setBt_area(String bt_area) {
		this.bt_area = bt_area;
	}
	public String getBt_addr() {
		return bt_addr;
	}
	public void setBt_addr(String bt_addr) {
		this.bt_addr = bt_addr;
	}
	public String getBt_status() {
		return bt_status;
	}
	public void setBt_status(String bt_status) {
		this.bt_status = bt_status;
	}
	public double getBt_lat() {
		return bt_lat;
	}
	public void setBt_lat(double bt_lat) {
		this.bt_lat = bt_lat;
	}
	public double getBt_lon() {
		return bt_lon;
	}
	public void setBt_lon(double bt_lon) {
		this.bt_lon = bt_lon;
	}
	
}
